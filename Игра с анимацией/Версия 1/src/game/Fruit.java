package game;

import java.awt.*;

public class Fruit {
    private Long id;
    private Image image;

    public Fruit(Long id, Image image) {
        this.id = id;
        this.image = image;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Image getImage() {
        return image;
    }

    public void setImage(Image image) {
        this.image = image;
    }
}
